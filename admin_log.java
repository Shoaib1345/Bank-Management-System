    private void loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginActionPerformed
        // TODO add your handling code here:
         try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            String dbURL = "jdbc:mysql://localhost:3306/db_connect";
            String username = "root";
            String password = "";
            try (Connection con = DriverManager.getConnection(dbURL, username, password)) {
                String query = "SELECT * FROM admin WHERE Name=? AND Pin=?";
                PreparedStatement pst = con.prepareStatement(query);
                pst.setString(1, admin_text.getText());
                pst.setString(2, new String(admin_pass.getPassword()));
                ResultSet rs = pst.executeQuery();
                if (rs.next()) {
                    // Open the operations frame if login is successful
                    String LoggedInUser = rs.getString("Name"); // Assuming the column name is "Name"
                   new admin_inter().setVisible(true);
                    this.setVisible(false);
                } else {
                    JOptionPane.showMessageDialog(this, "Invalid username or password.");
                }
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Database connection error: " + ex.getMessage());
        } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage());
        }
    }//GEN-LAST:event_loginActionPerformed

